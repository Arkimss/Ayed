package ejercicio_4;

public class Dato {
	String elem;
	int cant;
	
	public Dato() {
		this.cant = 0;
	}
	
	public String getElem() {
		return elem;
	}
	public void setElem(String elem) {
		this.elem = elem;
	}
	public int getCant() {
		return cant;
	}
	public void incCant() {
		this.cant ++ ;
	}	
	
}
